
public class ObjKaryawan {
	String namaKaryawan, kodeKaryawan, jenisKelamin, jabatan;
	int gaji;
	public ObjKaryawan(String namaKaryawan, String kodeKaryawan, String jenisKelamin, String jabatan, int gaji) {
		super();
		this.namaKaryawan = namaKaryawan;
		this.kodeKaryawan = kodeKaryawan;
		this.jenisKelamin = jenisKelamin;
		this.jabatan = jabatan;
		this.gaji = gaji;
	}
}
